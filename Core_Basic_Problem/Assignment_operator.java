public class Assignment_operator {
    public static void main(String[] args) {

        // Basic assignment operator
        int a = 10;
        System.out.println(a);

        // compound assignment operator
        a+=5; //15
        System.out.println(a);

        a-=3; //12
        System.out.println(a);

        a*=2; //24
        System.out.println(a);

        a/=2; //12
        System.out.println(a);

        a%=5; //2
        System.out.println(a);
    }
}
